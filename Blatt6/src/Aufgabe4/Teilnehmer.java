package Aufgabe4;

public class Teilnehmer {
	String name;
	int Telefonnummer;
	
	public void setName(String name) {
		 this.name = name;
	}
	public String getName() {
		return this.name;
	}
	public void setTelefon(int nummer) {
		this.Telefonnummer = nummer;
	}
	public int getTelefon() {
		return this.Telefonnummer;
	}
	

}
